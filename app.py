def fetch_openmeteo(lat, lon, model_key):
    try:
        r = requests.get('https://api.open-meteo.com/v1/forecast', params={
            'latitude': lat, 'longitude': lon,
            'hourly': 'windspeed_10m,winddirection_10m,windgusts_10m,surface_pressure',
            'models': model_key,
            'windspeed_unit': 'kmh',
            'timezone': 'Europe/Istanbul',
            'forecast_days': 7
        }, timeout=10)
        if r.ok: return r.json()
    except: pass
    return {}

def fetch_metno_ocean(lat, lon):
    """MET Norway Oceanforecast — dalga, SST, akıntı (ücretsiz)"""
    try:
        r = requests.get(
            'https://api.met.no/weatherapi/oceanforecast/2.0/complete',
            params={'lat': round(lat,3), 'lon': round(lon,3)},
            headers={'User-Agent': 'MarmAv-Analysis/1.0 (research)'},
            timeout=10
        )
        if r.ok:
            ts = r.json().get('properties',{}).get('timeseries',[])
            rows = []
            for t in ts[:168]:  # 7 gün
                det = t.get('data',{}).get('instant',{}).get('details',{})
                rows.append({
                    'time':         t['time'],
                    'wave_height':  det.get('sea_surface_wave_height'),
                    'wave_dir':     det.get('sea_surface_wave_from_direction'),
                    'wave_period':  det.get('sea_surface_wave_mean_period'),
                    'sst':          det.get('sea_water_temperature'),
                    'current_spd':  det.get('sea_water_speed'),
                    'current_dir':  det.get('sea_water_to_direction'),
                })
            df = pd.DataFrame(rows)
            df['time'] = pd.to_datetime(df['time'])
            return df
    except: pass
    return pd.DataFrame()

