# 7 günlük en iyi levrek saatlerini bul
def adiff(a, b):
    d = abs(a-b); return min(d, 360-d)

# Tekirdağ örneği (lodos kıyısı, shore_facing=135)
spot_name = 'Tekirdağ'
spot      = SPOTS[spot_name]

d_trd = fetch_openmeteo(spot['lat'], spot['lon'], 'icon_eu')
h_trd = d_trd.get('hourly', {})

if h_trd:
    times_t = pd.to_datetime(h_trd['time'])
    ws_raw  = h_trd['windspeed_10m']
    wd_raw  = h_trd['winddirection_10m']
    wg_raw  = h_trd.get('windgusts_10m', ws_raw)
    pr_raw  = h_trd.get('surface_pressure', [1013]*len(times_t))

    # DataFrame oluşturup None değerleri temizle
    df_hourly = pd.DataFrame({
        'time': times_t,
        'ws': ws_raw,
        'wd': wd_raw,
        'wg': wg_raw,
        'pressure': pr_raw
    }).dropna()

    if not df_hourly.empty:
        times_t = df_hourly['time'].values
        ws_t    = df_hourly['ws'].values
        wd_t    = df_hourly['wd'].values
        wg_t    = df_hourly['wg'].values
        pr_t    = df_hourly['pressure'].values

        sf = spot['shore_facing']
        is_on   = [adiff(wd_t[i], sf) <= 45  for i in range(len(wd_t))]
        is_side = [45 < adiff(wd_t[i], sf) <= 90 for i in range(len(wd_t))]

        # Skor hesabı
        scores = []
        for i in range(len(times_t)):
            s = 0.0
            if is_on[i]:   s += 3.5
            elif is_side[i]: s += 1.5
            if 15 <= ws_t[i] <= 40: s += 2.0
            elif ws_t[i] > 40: s += 0.5
            # Basınç bonusu
            if pr_t[i] < 1005: s += 1.5
            elif pr_t[i] < 1010: s += 1.0
            scores.append(min(10.0, s))

        df_trd = pd.DataFrame({
            'time': times_t, 'ws': ws_t, 'wd': wd_t,
            'is_on': is_on, 'pressure': pr_t, 'score': scores
        })

        best = df_trd.nlargest(5, 'score')
        print(f'🎣 {spot_name} — En İyi 5 Levrek Saati (7 gün):')
        print(best[['time','ws','wd','is_on','pressure','score']].to_string(index=False))
