pres_grid = np.zeros_like(glo_p)

def get_pressure(lat, lon):
    d = fetch_openmeteo(lat, lon, 'icon_eu')
    h = d.get('hourly',{})
    if not h: return 1013.0
    pres = h.get('surface_pressure',[])
    return float(pres[0]) if pres else 1013.0

