    b = next((i for i,t in enumerate(bft_thresholds) if ws < t), 12)
    col = bft_colors[min(b,12)]

    ax.annotate('', xy=(d['lon'] + u*arrow_scale, d['lat'] + v*arrow_scale),
                xytext=(d['lon'], d['lat']),
                arrowprops=dict(arrowstyle='->', color=col, lw=2.5+b*0.2,
                                mutation_scale=18))
    ax.plot(d['lon'], d['lat'], 'o', ms=5, color=col, mec='white', mew=0.5)
