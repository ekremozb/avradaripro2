# Tüm spotlar için anlık rüzgar verisi al
from concurrent.futures import ThreadPoolExecutor

def get_current_wind(lat, lon):
    d = fetch_openmeteo(lat, lon, 'icon_eu')
    h = d.get('hourly',{})
    if not h: return None
    # Şimdiki saate en yakın değer
    now = datetime.datetime.now().strftime('%Y-%m-%dT%H:00')
    times = h.get('time',[])
    ws_list = h.get('windspeed_10m',[])
    wd_list = h.get('winddirection_10m',[])
    if not times: return None
    idx = 0
    if now in times: idx = times.index(now)
    ws = ws_list[idx] if ws_list else 0
    wd = wd_list[idx] if wd_list else 0
    return ws, wd

print('📡 Spot rüzgar verileri çekiliyor...')
wind_data = {}
with ThreadPoolExecutor(max_workers=10) as ex:
    futs = {ex.submit(get_current_wind, sp['lat'], sp['lon']): nm
            for nm, sp in SPOTS.items()}
    for fut, nm in futs.items():
        result = fut.result()
        if result:
            wind_data[nm] = {'ws': result[0], 'wd': result[1],
                              **SPOTS[nm]}
            print(f'  ✅ {nm}: {result[0]:.0f} km/h {result[1]:.0f}°')

print(f'\n{len(wind_data)} noktadan veri alındı')

