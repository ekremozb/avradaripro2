# NASA ERDDAP — tüm noktalar için klorofil verisi
ERDDAP_BASE = 'https://coastwatch.noaa.gov/erddap'
ERDDAP_DS   = 'noaacwNPPVIIRSchlaDaily'

def fetch_chl_point(lat, lon):
    url = (f'{ERDDAP_BASE}/griddap/{ERDDAP_DS}.json'
           f'?chlor_a[(last)][({lat:.3f})][({lon:.3f})]')
    try:
        r = requests.get(url, timeout=10)
        if r.ok:
            rows = r.json().get('table',{}).get('rows',[])
            if rows and rows[0][-1] is not None:
                return float(rows[0][-1])
    except: pass
    return None

# 20-nokta ızgara — Marmara üzerinde
grid_lats = np.linspace(40.4, 41.4, 8)
grid_lons = np.linspace(26.2, 30.1, 12)
glo, gla  = np.meshgrid(grid_lons, grid_lats)

print('🛰️ NASA ERDDAP klorofil verisi çekiliyor (paralel)...')
chl_vals = np.zeros_like(glo) + np.nan

with ThreadPoolExecutor(max_workers=12) as ex:
    futs = {}
    for i in range(gla.shape[0]):
        for j in range(gla.shape[1]):
            futs[ex.submit(fetch_chl_point, gla[i,j], glo[i,j])] = (i,j)
    for fut, (i,j) in futs.items():
        v = fut.result()
        if v is not None and 0 < v < 50:
            chl_vals[i,j] = v

valid = np.sum(~np.isnan(chl_vals))
print(f'  ✅ {valid}/{gla.size} noktadan gerçek veri')

# Proxy: veri gelmezse mevsimsel ortalama kullan
if valid < 5:
    print('  ⚠️ ERDDAP verisi yetersiz — proxy model kullanılıyor')
    for i in range(gla.shape[0]):
        for j in range(gla.shape[1]):
            # Kıyıya yakınlık bazlı proxy
            coast_dist = min(abs(gla[i,j]-41.1), abs(gla[i,j]-40.4),
                            abs(glo[i,j]-27.0), abs(glo[i,j]-29.5))
            chl_vals[i,j] = max(0.3, 2.5 - coast_dist * 1.2 + np.random.uniform(0,0.3))

# NaN → interpolate
if np.any(np.isnan(chl_vals)):
    pts_valid = [(glo[i,j], gla[i,j]) for i in range(gla.shape[0])
                 for j in range(gla.shape[1]) if not np.isnan(chl_vals[i,j])]
    vals_valid = [chl_vals[i,j] for i in range(gla.shape[0])
                  for j in range(gla.shape[1]) if not np.isnan(chl_vals[i,j])]
    if pts_valid:
        chl_interp = griddata(pts_valid, vals_valid, (glo, gla), method='cubic')
        chl_vals = np.where(np.isnan(chl_vals), chl_interp, chl_vals)
    chl_vals = np.where(np.isnan(chl_vals), 0.5, chl_vals)

chl_smooth = gaussian_filter(np.log1p(chl_vals), sigma=1.0)
